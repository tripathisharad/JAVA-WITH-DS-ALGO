package operators;

public class AssignmentOperator {

	public static void main(String[] args) {
		
	int b = 2;
	b += 2;///b = b+2
		
	int c = 8;
    c-=6;///c = c-8
    int d = 45;
    d %=4;
    int e = 3;
    e <<=5;
    
    
    
	System.out.println(c);
	System.out.println(b);
	System.out.println(d);
	System.out.println(e);

	}

}
